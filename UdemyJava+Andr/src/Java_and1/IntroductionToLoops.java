
package Java_and1;

public class IntroductionToLoops {
    public static void main(String[] args) {
        //the while Loop
        int x=0;
        while(x<5){
            System.out.println("Hello World!");
            x++;
        }
        System.out.println();
        //the do-while Loop
        int y=0;
        do{
            System.out.println("Hello World!");
            y++;
        }while(y<5);
        System.out.println();
        //the for Loop
        for(int i=0;i<5;i++){
            System.out.println("Hello World!");
        }
    }
}
